#!/usr/bin/env node
/**
 * Extract logo SVG path data into typed TS modules.
 *
 * Reads packages/ui/src/brand-assets/source/primary-light/*.svg as the
 * canonical geometry (subpixel rounding differences between color folders
 * are visually invisible — one set of paths drives all tones). Tags each
 * path as "mark" or "text" based on its fill color so the runtime Logo
 * component can apply the right brand token per role.
 *
 *   #009473 (emerald, the mark glyph in primary-light) -> role: "mark"
 *   #0A1F1A (ink, the wordmark letters in primary-light) -> role: "text"
 *
 * Outputs packages/ui/src/brand-assets/paths/{variant}.ts modules.
 * Re-runnable: commit both source/ and paths/ — re-extracts on demand.
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs"
import { resolve, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __filename = fileURLToPath(import.meta.url)
const REPO = resolve(dirname(__filename), "..")
const SOURCE = resolve(
  REPO,
  "packages/ui/src/brand-assets/source/primary-light",
)
const PATHS_DIR = resolve(REPO, "packages/ui/src/brand-assets/paths")

const VARIANTS = ["horizontal", "stacked", "logomark", "wordmark"]

// Fill values that mark a path as "mark" (the wallet/brand glyph) vs
// "text" (the wordmark letters). Source SVGs use these exact hex values
// in the primary-light color folder.
const MARK_FILL = "#009473"
const TEXT_FILL = "#0A1F1A"

function roleForFill(fill) {
  const f = fill.toUpperCase()
  if (f === MARK_FILL.toUpperCase()) return "mark"
  if (f === TEXT_FILL.toUpperCase()) return "text"
  throw new Error(`unknown fill ${fill} — extend roleForFill()`)
}

function extract(svgPath) {
  const svg = readFileSync(svgPath, "utf-8")
  const viewBoxMatch = svg.match(/viewBox="([^"]+)"/)
  if (!viewBoxMatch) throw new Error(`no viewBox in ${svgPath}`)
  const viewBox = viewBoxMatch[1]

  // Pull every <path d="..." fill="..."> in document order.
  const paths = []
  const re = /<path\s+d="([^"]+)"\s+fill="([^"]+)"/g
  let m
  while ((m = re.exec(svg)) !== null) {
    paths.push({ d: m[1], role: roleForFill(m[2]) })
  }
  if (paths.length === 0) throw new Error(`no paths in ${svgPath}`)
  return { viewBox, paths }
}

function emit(variant, data) {
  const banner = [
    "// AUTO-GENERATED by scripts/build-logo-paths.mjs from",
    `// packages/ui/src/brand-assets/source/primary-light/${variant}.svg`,
    "// Do not edit by hand. Re-run the script to regenerate.",
  ].join("\n")

  const body = [
    "",
    'import type { LogoPathSet } from "../logo-types"',
    "",
    `export const ${variant.toUpperCase()}_PATHS: LogoPathSet = ${JSON.stringify(data, null, 2)}`,
    "",
  ].join("\n")

  mkdirSync(PATHS_DIR, { recursive: true })
  writeFileSync(resolve(PATHS_DIR, `${variant}.ts`), banner + body)
  console.log(
    `  ${variant.padEnd(12)} ${data.paths.length} path(s) (${data.paths.filter((p) => p.role === "mark").length} mark, ${data.paths.filter((p) => p.role === "text").length} text)`,
  )
}

console.log("extracting logo paths from primary-light canonical SVGs")
for (const variant of VARIANTS) {
  const data = extract(resolve(SOURCE, `${variant}.svg`))
  emit(variant, data)
}
console.log("done")
