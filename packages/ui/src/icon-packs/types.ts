import type * as React from "react"

/**
 * Common interface every icon component must satisfy. Extends
 * `SVGProps` so callers can pass `size`, `onClick`, `aria-label`,
 * etc — lucide and phosphor both accept this surface. The pack-
 * specific extras (lucide's `strokeWidth`, phosphor's `weight` +
 * `mirrored`) are NOT in the common type — pass them via the
 * pack's own component import if you need them.
 */
export type IconComponent = React.ComponentType<React.SVGProps<SVGSVGElement>>

