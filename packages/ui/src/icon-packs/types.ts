export type IconName = (typeof ICON_NAMES)[number]

/**
 * Exhaustive icon map: every `IconName` MUST resolve to an
 * `IconComponent`. `satisfies IconMap` on a pack will TypeScript-error
 * if a key is missing, plus the CI parity script catches drift on PRs
 * that touch only one pack.
 */
export type IconMap = Record<IconName, IconComponent>

/**
 * Identifier for an installed icon pack. Add new packs to this union
 * and to the `PACKS` lookup in `icon-context.tsx`.
 */
export type IconPackName = "lucide" | "phosphor" | "fontawesome"
